      private ChartPanel createPanel(List<Zivotinja> zivotinje) {
        ChartPanel panel = new ChartPanel(createChart(createDataset(zivotinje)));
        panel.setPreferredSize(new java.awt.Dimension(800, 600));
        return panel;
    }
      
      private JFreeChart createChart(PieDataset dataset) {
        JFreeChart chart = ChartFactory.createPieChart("Broj Å¾ivotinja s kojima rade djelatnici", dataset, true, true, false);
        PiePlot plot = (PiePlot) chart.getPlot();
        plot.setSectionPaint("Djelatnik 1", new Color(255, 100, 100));
        plot.setSectionPaint("Djelatnik 2", new Color(100, 255, 100));
        plot.setSectionPaint("Djelatnik 3", new Color(100, 100, 255));
        plot.setSectionPaint("Djelatnik 4", new Color(255, 255, 100));
        plot.setSectionPaint("Djelatnik 5", new Color(255, 100, 255));
        plot.setSectionPaint("Djelatnik 6", new Color(100, 255, 255));
        return chart;
    }
      
    private PieDataset createDataset(List<Zivotinja> zivotinje) {
        DefaultPieDataset dataset = new DefaultPieDataset();
        Map<String, Integer> counts = new HashMap<>();
        for (Zivotinja zivotinja : zivotinje) {
            String p = zivotinja.getDjelatnik().getIme() + " " + zivotinja.getDjelatnik().getPrezime();
            counts.put(p,counts.getOrDefault(p, 0) + 1);
        }
        for (String p : counts.keySet()) {
             dataset.setValue(p + " (" + counts.get(p) + ")", counts.get(p));
        }
        return dataset;
    }  
      
    
     
